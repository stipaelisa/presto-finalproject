     */
    public function show(Article $article)
    {
        $correlateArticles = Article::where('category_id', $article->category->id)
            ->where('is_accepted', true)
            ->where('id', '!=', $article->id)
            ->orderBy('created_at', 'desc')
            ->take(4)
            ->get();
        return view ('articles.show', compact('article', 'correlateArticles'));
    }

